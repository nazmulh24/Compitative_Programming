void solve()
{
    int k, l, m, n, d;
    cin >> k >> l >> m >> n >> d;

    int damaged_dragons = 0;

    for (int i = 1; i <= d; i++)
    {
        if (i % k == 0 || i % l == 0 || i % m == 0 || i % n == 0)
            damaged_dragons++;
    }

    cout << damaged_dragons << endl;
}

int32_t main()
{
    ios::sync_with_stdio(0);
    cin.tie(0);

    int t = 1;
    // cin >> t; // Uncomment if multiple test cases

    while (t--)
    {
        solve();
    }

    return 0;
}
